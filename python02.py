# คำสั้งที่ใช้ในการแสดงผลข้อมูลใดๆ ที่หน้าจอ คือ print()
# String ข้อความ
print('AAAA')            # double quote 
print("BBB")             # single quote 
print('''CCC''')         # triple quote by single quote
print("""DDD""")         # triple quote by double quote

# Number ตัวเลข
print(12345)             # integer 
print(12.345)            # float

# Boolean  ตรรกะ (True/False)
print(True)
print(False)

# Expression นิพจ คือ อะไรที่มี Process การทำงาน
print(10 +20 +30)
print(5 * 50)