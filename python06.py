# Operator ตัวดำเนินการ/เครื่องหมายในการทำ process การทำงานต่างๆ
# Arithematic Opt. + - * / % // **
print(10 + 3) # 13
print(10 - 3) # 7
print(10 * 3) # 30
print(10 / 3)   # หารธรรมดา
print(10.0 / 3) # หารธรรมดา
print(10 % 3)   # หารเอาเศษ (mod) 1
print(10 // 3)  # หารเอาส่วน (floor) 3
print(10 ** 3)  # 1000